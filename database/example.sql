DROP DATABASE IF EXISTS `entities`;
CREATE DATABASE IF NOT EXISTS `entities`;
USE `entities`;

DROP TABLE IF EXISTS `Club`;
CREATE TABLE `Club` (
  `clubId` INT AUTO_INCREMENT NOT NULL,
  `name` VARCHAR(255),
  `description` VARCHAR(255),
  PRIMARY KEY (`clubId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Department`;
CREATE TABLE `Department` (
  `departmentId` INT AUTO_INCREMENT NOT NULL,
  `name` VARCHAR(255),
  `building` VARCHAR(255),
  `budget` FLOAT,
  PRIMARY KEY (`departmentId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Classroom`;
CREATE TABLE `Classroom` (
  `classroomId` INT AUTO_INCREMENT NOT NULL,
  `building` VARCHAR(255),
  `roomNumber` VARCHAR(255),
  `capacity` INT,
  PRIMARY KEY (`classroomId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Major`;
CREATE TABLE `Major` (
  `majorId` INT AUTO_INCREMENT NOT NULL,
  `name` VARCHAR(255),
  `departmentId` INT,
  PRIMARY KEY (`majorId`),
  CONSTRAINT `fk_Major_departmentId` FOREIGN KEY (`departmentId`) REFERENCES `Department`(`departmentId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Professor`;
CREATE TABLE `Professor` (
  `professorId` INT AUTO_INCREMENT NOT NULL,
  `firstName` VARCHAR(255),
  `lastName` VARCHAR(255),
  `email` VARCHAR(255),
  `hireDate` DATE,
  `departmentId` INT,
  PRIMARY KEY (`professorId`),
  CONSTRAINT `fk_Professor_departmentId` FOREIGN KEY (`departmentId`) REFERENCES `Department`(`departmentId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Course`;
CREATE TABLE `Course` (
  `courseId` VARCHAR(255) NOT NULL,
  `title` VARCHAR(255),
  `credits` INT,
  `departmentId` INT,
  PRIMARY KEY (`courseId`),
  CONSTRAINT `fk_Course_departmentId` FOREIGN KEY (`departmentId`) REFERENCES `Department`(`departmentId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Student`;
CREATE TABLE `Student` (
  `studentId` INT AUTO_INCREMENT NOT NULL,
  `firstName` VARCHAR(255),
  `lastName` VARCHAR(255),
  `email` VARCHAR(255),
  `dateOfBirth` DATE,
  `enrollmentYear` INT,
  `majorId` INT,
  PRIMARY KEY (`studentId`),
  CONSTRAINT `fk_Student_majorId` FOREIGN KEY (`majorId`) REFERENCES `Major`(`majorId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Schedule`;
CREATE TABLE `Schedule` (
  `scheduleId` INT AUTO_INCREMENT NOT NULL,
  `courseId` VARCHAR(255),
  `professorId` INT,
  `classroomId` INT,
  `dayOfWeek` VARCHAR(255),
  `startTime` TIME,
  `endTime` TIME,
  `semester` VARCHAR(255),
  PRIMARY KEY (`scheduleId`),
  CONSTRAINT `fk_Schedule_courseId` FOREIGN KEY (`courseId`) REFERENCES `Course`(`courseId`),
  CONSTRAINT `fk_Schedule_professorId` FOREIGN KEY (`professorId`) REFERENCES `Professor`(`professorId`),
  CONSTRAINT `fk_Schedule_classroomId` FOREIGN KEY (`classroomId`) REFERENCES `Classroom`(`classroomId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `ClubMembership`;
CREATE TABLE `ClubMembership` (
  `membershipId` INT AUTO_INCREMENT NOT NULL,
  `studentId` INT,
  `clubId` INT,
  `joinDate` DATE,
  PRIMARY KEY (`membershipId`),
  CONSTRAINT `fk_ClubMembership_studentId` FOREIGN KEY (`studentId`) REFERENCES `Student`(`studentId`),
  CONSTRAINT `fk_ClubMembership_clubId` FOREIGN KEY (`clubId`) REFERENCES `Club`(`clubId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Submission`;
CREATE TABLE `Submission` (
  `submissionId` INT AUTO_INCREMENT NOT NULL,
  `studentId` INT,
  `courseId` VARCHAR(255),
  `title` VARCHAR(255),
  `submittedAt` DATETIME,
  PRIMARY KEY (`submissionId`),
  CONSTRAINT `fk_Submission_studentId` FOREIGN KEY (`studentId`) REFERENCES `Student`(`studentId`),
  CONSTRAINT `fk_Submission_courseId` FOREIGN KEY (`courseId`) REFERENCES `Course`(`courseId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS `Enrollment`;
CREATE TABLE `Enrollment` (
  `enrollmentId` INT AUTO_INCREMENT NOT NULL,
  `studentId` INT,
  `scheduleId` INT,
  `grade` VARCHAR(255),
  PRIMARY KEY (`enrollmentId`),
  CONSTRAINT `fk_Enrollment_studentId` FOREIGN KEY (`studentId`) REFERENCES `Student`(`studentId`),
  CONSTRAINT `fk_Enrollment_scheduleId` FOREIGN KEY (`scheduleId`) REFERENCES `Schedule`(`scheduleId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


